         <br><b>2.1. Garantias da Política de Segurança da Informação</b><br>
         <br>A Política de Segurança da Informação do Camões, I.P. pretende garantir que: <br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os ativos de informação serão protegidos contra todos os acessos não autorizados;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;A confidencialidade da informação será preservada;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os riscos serão mitigados;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os incidentes de segurança serão prevenidos;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;O potencial impacto desses incidentes será reduzido;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;A integridade e disponibilidade da informação serão mantidas;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os requisitos legais, legislativos, regulatórios e contratuais serão cumpridos;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os planos de continuidade de negócio serão definidos, mantidos e testados;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Será disponibilizada formação em segurança de informação para todos os trabalhadores;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Todos os incidentes relacionados com a segurança de informação serão reportados e adequadamente investigados;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Todas as equipas estarão comprometidas com, e colaborarão ativamente para, o cumprimento da Política de Segurança da Informação e melhoria contínua do Sistema de Gestão do Camões, I.P.;<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Os dados Pessoais serão objeto de um tratamento lícito, leal e transparente em relação ao Titular dos Dados e serão recolhidos para finalidades determinadas, explícitas e legítimas não sendo tratados de uma forma incompatível com essas finalidades.
         <br><br><b>2.2. Cumprimento da Política de Segurança da Informação</b><br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;O Serviço de Informática é o responsável pela manutenção da Política de Segurança da Informação e pelo suporte e aconselhamento durante a sua implementação e manutenção.<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;Todas as chefias intermédias são responsáveis pela implementação e cumprimento desta Política nas suas respectivas áreas/unidades orgânicas (UO).<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;O cumprimento da Política de Segurança da Informação é obrigatório.<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;A Política de Segurança da Informação está alinhada com Missão e atribuições do Camões, I.P. bem como com a estratégia de gestão do risco definida no Sistema de Gestão do Camões, I.P.<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;O critério de avaliação dos riscos, utilizado na metodologia do cálculo de risco definida no Sistema de Gestão de Segurança da Informação (SGSI), baseia-se no impacto da ocorrência deste para a confidencialidade, integridade e disponibilidade da informação.<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;A Política de Segurança da Informação, bem como as restantes políticas que lhe dão suporte, deverão ser revistas sempre que ocorram alterações que o justifiquem, pelo Serviço de Informática, com conhecimento do Encarregado da Proteção de Dados (EPD) e aprovadas pelo Conselho Diretivo (CD).<br>
         <i class="	fa fa-angle-right" style="color: #888C00;"></i>&nbsp;A Política de Segurança é alicerçada pelas restantes políticas presentes neste Manual que lhe dão suporte para o tratamento dos dados pessoais em conformidade com o RGPD.

